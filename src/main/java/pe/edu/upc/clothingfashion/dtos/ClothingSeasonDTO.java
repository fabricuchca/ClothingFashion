package pe.edu.upc.clothingfashion.dtos;

public class ClothingSeasonDTO {
    private String nameSeason;
    private int quantityClothing;

    public String getNameSeason() {
        return nameSeason;
    }

    public void setNameSeason(String nameSeason) {
        this.nameSeason = nameSeason;
    }

    public int getQuantityClothing() {
        return quantityClothing;
    }

    public void setQuantityClothing(int quantityClothing) {
        this.quantityClothing = quantityClothing;
    }
}
